package chpt_02;

public class Ch2_01 {

	public static void main(String[] args) {
		// 변수의 초기화
		int year = 0;
		int age = 14;
		
		System.out.println("1.year = " + year);
		System.out.println("1.age = " + age);
		
		year = age + 2000;
		age = age + 1;
		
		System.out.println("2.year = " + year);
		System.out.println("2.age = " + age);
	}

}
